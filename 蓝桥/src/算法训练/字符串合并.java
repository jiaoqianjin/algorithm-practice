package 算法训练;

import java.util.Scanner;

/**
 * Description：
 *输入两个字符串，将其合并为一个字符串后输出。
 * @author jiaoqianjin
 * Date: 2020/5/26 16:32
 **/

public class 字符串合并 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String s1 = sc.next();
        String s2 = sc.next();
        System.out.println(s1+s2);
    }
}
